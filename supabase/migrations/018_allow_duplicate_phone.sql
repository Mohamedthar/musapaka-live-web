-- يلغي قيد UNIQUE على حقل phone في جدول students
-- لأن رقم الهاتف قد يكون مشتركاً بين أكثر من طالب (إخوة - نفس ولي الأمر)
-- =================================================================================================

-- حذف قيد uniqueness على الهاتف
ALTER TABLE students DROP CONSTRAINT IF EXISTS students_phone_unique;

-- حذف الفهرس المناظر (إن وجد)
DROP INDEX IF EXISTS idx_students_phone;

-- إعادة إنشاء الفهرس كفهرس عادي (بدون UNIQUE) للبحث السريع
CREATE INDEX IF NOT EXISTS idx_students_phone ON students(phone);
